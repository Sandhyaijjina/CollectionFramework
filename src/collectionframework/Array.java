package collectionframework;

public class Array {

	public static void main(String[] args) {
		
		int studentID[] = new int[5];
		
		studentID[0] = 123456;
		studentID[1] = 345678;
		studentID[2] = 765473;
		studentID[3] = 654783;
		studentID[4] = 765489;
		
		System.out.println("Student ID is 0: " + studentID[0]);
		System.out.println("Student ID is 1: " + studentID[1]);
		System.out.println("Student ID is 2: " + studentID[2]);
		System.out.println("Student ID is 3: " + studentID[3]);
		System.out.println("Student ID is 4: " + studentID[4]);
	}

}
